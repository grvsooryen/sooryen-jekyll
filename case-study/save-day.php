<div class="row case-solution white-background-full border-D-top">
	<div class="large-10 large-centered columns text-center">
		<h2>Solution</h2>
		<p>When SaveDay approached Sooryen to develop their Financial Product, they were staffed with a “full stack” team of designers, front and back end developers, project managers and architects. A complete product team that would leverage agile methodology with 2 week SCRUM cycles, rapidly iterating through multiple options to find optimum solutions. Time-to-market, by design.<br><br>

		Most importantly, Sooryen could bring its deep subject matter expertise (in Financial services) to bear. The stringent software security expectations within the Financial industry along with requirements for audit trails and compliance. A deep understanding of technology required for building complex trading platforms, advisor platform, robot trading and rich user experience across various OS and form factors.<br><br>

		With the Sooryen team, SaveDay was able to launch their new 401(k) product in record time, helping change how we think about our retirement.

